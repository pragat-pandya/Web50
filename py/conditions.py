# Unlike many programming languages in python indentation matters!
# It's like python's syntax forces you to write pretty looking code ! (Enforces good style by syntax)
num = int(input("Please enter an integer: "))
if num > 0:
  print ("You've entered a positive number")
elif num < 0:
  print ("You've entered a negative number")
else:
  print ("You've entered a ZERO")