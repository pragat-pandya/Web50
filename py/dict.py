# Dictionaries are key-value pairs (hash-table) and are defined in following manner:

designations =  {"Janitor":"Claudie", "Manager":"Paulo", "Accountant":"Carry", "Marketing":"Sam"}

# This prints the whole dictionary
print(designations)

# This prints the value associated with subscripted key
print(designations["Janitor"])

# Dictionary in python: Ordered-NO, Mutable-YES