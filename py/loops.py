# For loops
for i in range(0,11):
  print(i)

print() 

for i in ["a", "b","c"]:
  print (i)

print()

names = ["julia", "Rachel", "Mike", "Susan"]
for name in names:
  print(name)

print()

s = "string"
for char in s:
  print(char)

print()

# While loops
j = 10
while j > 0:
  print(j)
  j = j - 1 

