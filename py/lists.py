# Lists allows us to group objcets of various types under one ubmrella sequence 

# Lists can be defines as following
l = ["Pragat", "Rahul", "Sudarshan", 90, 3.44, True, None]

# We can either print the whole list
print(l)

# Or else we can print an individual element of the list using array like subscripting
print(l[2])

# We can add a new item at the end of the list
l.append ("New item")
print(l)

